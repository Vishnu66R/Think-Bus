-- Students Table
-- Stores student-specific information, relates them to their parents and preferred routes

CREATE TABLE IF NOT EXISTS students (
    id INT PRIMARY KEY,
    user_id INT UNIQUE NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    full_name TEXT NOT NULL,
    adm_number TEXT UNIQUE,
    semester TEXT,
    department TEXT,
    
    parent_id INT REFERENCES parents(id) ON DELETE SET NULL,
    
    -- Boarding preferences 
    boarding_stop_id INT REFERENCES route_stops(id) ON DELETE SET NULL,
    default_route_id INT REFERENCES routes(id) ON DELETE SET NULL,
    current_bus_id INT REFERENCES buses(id) ON DELETE SET NULL,
    
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

